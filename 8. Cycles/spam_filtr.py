# Цель:
# Научиться использовать операторы break , continue и else на примере простого спам-фильтра для текстовых сообщений.
# Задача:
# Проверить массив сообщений на наличие слова "spam".
# Остановить проверку и вывести "Найден spam", если слово обнаружено.
# Пропускать длинные сообщения(более 20 символов).
# Уведомить о завершении проверки, если спам не обнаружен.
# Алгоритм:
# Итерация по сообщениям:
# Использовать цикл for для перебора каждого сообщения.
# Проверка на "&#x73;pa&#x6D;":
# Если сообщение содержит слово "spam":
# Вывести "Найден spam".
# Использовать оператор break для завершения проверки.
# Пропуск длинных сообщений:
# Если длина сообщения превышает 20 символов:
# Использовать оператор continue для перехода к следующему сообщению.
# Вывод итогов:
# Если цикл завершился без использования break , использовать else для вывода "Проверка завершена, spam не найден".
# Тестирование:
# Проверить сценарии, где сообщения содержат слово "spam".
# Изменить слово "spam" в сообщении, чтобы убедиться, что фильтр работает правильно.
# Проверить, что длинные сообщения не обрабатываются.

messages = ["Hello, how are you?", "Buy our product now!",
            "spam - the best way to make money", "Don't forget to subscribe!"]

for message in messages:
    if len(message) > 20:
        continue
    if "spam" in message:
        print("Found spam")
        break
else:
    print("No spam found")
