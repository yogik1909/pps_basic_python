# **Описание**: Создайте программу, которая выводит квадраты всех чисел от 1 до 8 с помощью цикла for
# **Входные данные**: Программа не требует внешних входных данных, используется диапазон от 1 до 8
# **Выходные данные**: Каждый квадрат числа выводится на отдельной строке в формате "число в квадрате = результат"
# **Ограничения**: Используйте цикл for с range() для перебора чисел от 1 до 8 включительно

for i in range(1, 9):
    print(f"{i} в квадрате = {i ** 2}")