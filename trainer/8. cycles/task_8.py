# **Описание**: Создайте программу, которая подсчитывает количество элементов в списке, которые больше заданного порогового значения
#
# **Входные данные**: Список чисел уже определен в коде как scores = [45, 78, 23, 89, 67, 34, 92, 56, 81, 29] и пороговое значение threshold = 60
#
    # **Выходные данные**: Количество элементов, превышающих пороговое значение (целое число)
    #
# **Ограничения**: Используйте цикл for для перебора всех элементов списка и условие if для сравнения с пороговым значением
#
# **Примеры**:
# Входные данные: scores = [45, 78, 23, 89, 67, 34, 92, 56, 81, 29], threshold = 60
# Выходные данные: 5
#
# Входные данные: scores = [10, 20, 30, 40], threshold = 25
# Выходные данные: 2

scores = [45, 78, 23, 89, 67, 34, 92, 56, 81, 29]
threshold = 60
count = 0
for score in scores:
    if score > threshold:
        count += 1
print(count)