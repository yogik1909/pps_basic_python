# **Описание**: Создайте программу, которая выводит все числа от 1 до 15, но останавливается, когда встречает число 8
# **Входные данные**: Программа не требует внешних входных данных, используется диапазон от 1 до 15
# **Выходные данные**: Каждое число (до числа 8 включительно) выводится на отдельной строке, после чего программа завершает работу
# **Ограничения**: Используйте цикл for с range() и оператор break для остановки цикла при достижении числа 8

for i in range(1, 16):
    if i == 8:
        break
    print(i)
