# **Описание**: Создайте программу, которая выводит все элементы списка в обратном порядке с помощью цикла for
#
# **Входные данные**: Список строк уже определен в коде как colors = ["красный", "синий", "зеленый", "желтый", "фиолетовый"]
#
# **Выходные данные**: Каждый элемент списка выводится на отдельной строке в обратном порядке
#
# **Ограничения**: Используйте цикл for с функцией reversed() для обхода списка в обратном порядке
#
# **Примеры**:
# Входные данные: colors = ["красный", "синий", "зеленый", "желтый", "фиолетовый"]
# Выходные данные:
# фиолетовый
# желтый
# зеленый
# синий
# красный
#
# Входные данные: colors = ["белый", "черный", "серый"]
# Выходные данные:
# серый
# черный
# белый

colors = ["красный", "синий", "зеленый", "желтый", "фиолетовый"]

for color in reversed(colors):
    print(color)