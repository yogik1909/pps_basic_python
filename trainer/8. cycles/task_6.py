# **Описание**: Создайте программу, которая подсчитывает количество отрицательных чисел в заданном списке
#
# **Входные данные**: Список чисел уже определен в коде как numbers = [-5, 3, -2, 8, -1, 0, 7, -9]
#
# **Выходные данные**: Количество отрицательных чисел (целое число)
#
# **Ограничения**: Используйте цикл for для перебора всех элементов списка и условие if для проверки отрицательности
#
# **Примеры**:
# Входные данные: numbers = [-5, 3, -2, 8, -1, 0, 7, -9]
# Выходные данные: 4
#
# Входные данные: numbers = [1, 2, 3, 4, 5]
# Выходные данные: 0

numbers = [-5, 3, -2, 8, -1, 0, 7, -9]
count = 0

for number in numbers:
    if number < 0:
        count += 1

print(count)