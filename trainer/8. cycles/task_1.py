# **Описание**: Напишите программу, которая подсчитывает общую стоимость покупок в магазине
# **Входные данные**: Список цен товаров уже определен в коде как prices = [15.50, 23.00, 8.75, 42.30, 12.90]
# **Выходные данные**: Общая сумма всех покупок (число с плавающей точкой)
# **Ограничения**: Используйте цикл for для перебора всех элементов списка
# **Примеры**:
# Входные данные: prices = [15.50, 23.00, 8.75, 42.30, 12.90]
# Выходные данные: 102.45
# Входные данные: prices = [10.0, 20.0, 30.0]
# Выходные данные: 60.0

prices = [15.50, 23.00, 8.75, 42.30, 12.90]
total = 0

# Ваш код здесь
for price in prices:
    total += price

print(total)