# **Описание**: Создайте программу, которая выводит все числа от 1 до 10, но пропускает числа, которые делятся на 3
# **Входные данные**: Программа не требует внешних входных данных, используется диапазон от 1 до 10
# **Выходные данные**: Каждое число (кроме кратных 3) выводится на отдельной строке
# **Ограничения**: Используйте цикл for с range() и оператор continue для пропуска чисел, кратных 3

for i in range(1, 11):
    if i % 3 == 0:
        continue
    print(i)