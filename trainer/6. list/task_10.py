# **Описание**: Создайте программу для работы с кортежем названий планет и их группировкой
#
# **Входные данные**: Кортеж planets содержащий названия планет
#
# **Выходные данные**:
# - Общее количество планет в кортеже
# - Четвертая планета в кортеже
# - Третья планета с конца кортеже
# - Новый кортеж с планетами, разделенными на две группы: первая половина и вторая половина
# - Новый кортеж где каждая планета объединена с символом "*" между ними
#
# **Ограничения**: Кортеж содержит четное количество планет (минимум 6)
#
# **Примеры**:
# Input: planets = ("Меркурий", "Венера", "Земля", "Марс", "Юпитер", "Сатурн")
# Output:
# Общее количество планет: 6
# Четвертая планета: Марс
# Третья с конца: Юпитер
# Первая половина: ("Меркурий", "Венера", "Земля")
# Вторая половина: ("Марс", "Юпитер", "Сатурн")
# С разделителями: ("Меркурий", "*", "Венера", "*", "Земля", "*", "Марс", "*", "Юпитер", "*", "Сатурн")
#
# Входные данные: planets = ("Нептун", "Уран", "Плутон", "Церера")
# Output:
# Общее количество планет: 4
# Четвертая планета: Церера
# Третья с конца: Уран
# Первая половина: ("Нептун", "Уран")
# Вторая половина: ("Плутон", "Церера")
# С разделителями: ("Нептун", "*", "Уран", "*", "Плутон", "*", "Церера")

planets = ("Меркурий", "Венера", "Земля", "Марс", "Юпитер", "Сатурн")

if len(planets) < 6 or len(planets) % 2 != 0:
    print("Ошибка: требуется чётное количество планет минимум 6")
    exit()

mid = len(planets)//2
first_half = planets[:mid]
second_half = planets[mid:]
star_line = ("*",) * (len(planets) - 1)
pairs = zip(planets, star_line)
with_separator = tuple(
    item for pair in pairs for item in pair) + (planets[-1],)
# - Общее количество планет в кортеже
print(f"Общее количество планет: {len(planets)}")
# - Четвертая планета в кортеже
print(f"Четвертая планета в кортеже: {planets[3]}")
# - Третья планета с конца кортеже
print(f"Третья планета с конца кортеже: {planets[-3]}")
# - Новый кортеж с планетами, разделенными на две группы: первая половина и вторая половина
print(
    f"Первая половина: {first_half}")
print(
    f"Вторая половина: {second_half}")
# - Новый кортеж где каждая планета объединена с символом "*" между ними
print(
    f"Новый кортеж где каждая планета объединена с символом \"*\": {with_separator}")

# ============================================================================
# РАЗВЕРНУТЫЙ КОД ДЛЯ СТРОКИ 44:
# ============================================================================
#
# with_separator = tuple(item for pair in pairs for item in pair)
#
# РАЗБОР ПО ЧАСТЯМ:
#
# 1. pairs = zip(planets, star_line)
#    - zip() создает пары элементов из двух последовательностей
#    - Для planets = ("Меркурий", "Венера", "Земля", "Марс", "Юпитер", "Сатурн")
#    - И star_line = "*****" (5 звездочек)
#    - pairs будет содержать: ("Меркурий", "*"), ("Венера", "*"), ("Земля", "*"),
#      ("Марс", "*"), ("Юпитер", "*")
#
# 2. item for pair in pairs
#    - ВНЕШНИЙ цикл: перебирает каждую пару из pairs
#    - pair будет последовательно: ("Меркурий", "*"), ("Венера", "*"), и т.д.
#
# 3. for item in pair
#    - ВНУТРЕННИЙ цикл: перебирает элементы внутри каждой пары
#    - Для пары ("Меркурий", "*") item будет: "Меркурий", затем "*"
#
# 4. tuple(...)
#    - Преобразует генератор в кортеж
#
# КАК ЭТО РАБОТАЕТ ПОШАГОВО:
#
# Итерация 1: pair = ("Меркурий", "*")
#   - item = "Меркурий" → добавляется
#   - item = "*" → добавляется
#   Результат пока: "Меркурий", "*"
#
# Итерация 2: pair = ("Венера", "*")
#   - item = "Венера" → добавляется
#   - item = "*" → добавляется
#   Результат пока: "Меркурий", "*", "Венера", "*"
#
# Итерация 3: pair = ("Земля", "*")
#   - item = "Земля" → добавляется
#   - item = "*" → добавляется
#   Результат пока: "Меркурий", "*", "Венера", "*", "Земля", "*"
#
# И так далее для всех пар...
#
# ИТОГОВЫЙ РЕЗУЛЬТАТ:
# ("Меркурий", "*", "Венера", "*", "Земля", "*", "Марс", "*", "Юпитер", "*", "Сатурн")
#
# ============================================================================
# РАЗВЕРНУТЫЙ ВАРИАНТ БЕЗ ГЕНЕРАТОРА:
# ============================================================================

# Создаем пустой список для результата
result_list = []

# Создаем пары через zip (как в оригинале)
pairs_expanded = zip(planets, star_line)

# Внешний цикл: перебираем каждую пару
for pair in pairs_expanded:
    # Внутренний цикл: перебираем элементы внутри пары
    for item in pair:
        # Добавляем каждый элемент в список
        result_list.append(item)

# Добавляем последнюю планету (так как zip останавливается на меньшей последовательности)
# В данном случае star_line короче на 1 элемент, поэтому последняя планета не попала в пары
if len(planets) > len(star_line):
    result_list.append(planets[-1])

# Преобразуем список в кортеж
with_separator_expanded = tuple(result_list)

print(f"\n--- РАЗВЕРНУТЫЙ ВАРИАНТ ---")
print(f"С разделителями (развернутый код): {with_separator_expanded}")

# ============================================================================
# ЕЩЕ БОЛЕЕ ЯВНЫЙ ВАРИАНТ:
# ============================================================================

result_list_alt = []
# Перебираем планеты по индексам
for i in range(len(planets)):
    # Добавляем планету
    result_list_alt.append(planets[i])
    # Если это не последняя планета, добавляем разделитель
    if i < len(planets) - 1:
        result_list_alt.append("*")

with_separator_alt = tuple(result_list_alt)
print(f"С разделителями (альтернативный вариант): {with_separator_alt}")
