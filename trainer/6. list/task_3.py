# **Описание**: Создайте программу для работы с кортежем студентов и их оценок
#
# **Входные данные**: Кортеж student_data содержащий информацию в формате (имя1, оценка1, имя2, оценка2, имя3, оценка3, ...)
#
# **Выходные данные**:
# - Общее количество элементов в кортеже
# - Имя первого студента
# - Оценка последнего студента
# - Новый кортеж содержащий только имена студентов (элементы на четных позициях)
# - Новый кортеж содержащий только оценки (элементы на нечетных позициях)
#
# **Ограничения**: Кортеж содержит четное количество элементов (минимум 4)
#
# **Примеры**:
# Input: student_data = ("Анна", 85, "Борис", 92, "Вера", 78)
# Output:
# Общее количество элементов: 6
# Имя первого студента: Анна
# Оценка последнего студента: 78
# Имена студентов: ("Анна", "Борис", "Вера")
# Оценки: (85, 92, 78)
#
# Входные данные: student_data = ("Дима", 90, "Елена", 88)
# Output:
# Общее количество элементов: 4
# Имя первого студента: Дима
# Оценка последнего студента: 88
# Имена студентов: ("Дима", "Елена")
# Оценки: (90, 88)

# Встроенные данные для работы
student_data = ("Анна", 85, "Борис", 92, "Вера", 78)

# Ваш код здесь
print(f"Общее количество элементов: {len(student_data)}")
print(f"Имя первого студента: {student_data[0]}")
print(f"Оценка последнего студента: {student_data[-1]}")
print(f"Имена студентов: {student_data[::2]}")
print(f"Оценки: {student_data[1::2]}")
