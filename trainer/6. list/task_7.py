# **Описание**: Создайте программу для работы с данными о фильмах и их копированием
#
# **Входные данные**: Список movies содержащий названия фильмов
#
# **Выходные данные**:
# - Поверхностная копия исходного списка
# - Глубокая копия исходного списка (через срез)
# - Проверка равенства исходного списка и поверхностной копии
# - Проверка идентичности исходного списка и поверхностной копии
# - Проверка идентичности исходного списка и глубокой копии
#
# **Ограничения**: Список содержит минимум 3 фильма
#
# **Примеры**:
# Input: movies = ["Титаник", "Аватар", "Матрица"]
# Output:
# Поверхностная копия: ["Титаник", "Аватар", "Матрица"]
# Глубокая копия: ["Титаник", "Аватар", "Матрица"]
# Равенство (==): True
# Идентичность поверхностной копии (is): False
# Идентичность глубокой копии (is): False
#
# Входные данные: movies = ["Звездные войны", "Гарри Поттер"]
# Output:
# Поверхностная копия: ["Звездные войны", "Гарри Поттер"]
# Глубокая копия: ["Звездные войны", "Гарри Поттер"]
# Равенство (==): True
# Идентичность поверхностной копии (is): False
# Идентичность глубокой копии (is): False

movies = ["Титаник", "Аватар", "Матрица"]

# Создаем отдельные переменные для копий (чтобы не создавать новую копию каждый раз при проверке)
# Поверхностная копия через срез (новый объект списка)
shallow_copy = movies
copy_slice = movies[:]
# Глубокая копия через срез (для простых списков это тоже поверхностная копия)
deep_copy = movies[:]

# - Поверхностная копия исходного списка
print(f"Поверхностная копия: {shallow_copy}")
# - Глубокая копия исходного списка (через срез)
print(f"Глубокая копия: {deep_copy}")
# - Проверка равенства исходного списка и поверхностной копии
print(f"Равенство (==): {movies == shallow_copy}")
# - Проверка идентичности исходного списка и поверхностной копии
print(f"Идентичность поверхностной копии (is): {movies is shallow_copy}")
# - Проверка идентичности исходного списка и глубокой копии
print(f"Идентичность глубокой копии (is): {movies is deep_copy}")
