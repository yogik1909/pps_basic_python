# **Описание**: Создайте программу для работы с кортежем цветов и их дублированием
#
# **Входные данные**: Кортеж colors содержащий названия цветов
#
# **Выходные данные**:
# - Общее количество цветов в кортеже
# - Второй цвет в кортеже
# - Предпоследний цвет в кортеже
# - Новый кортеж где каждый цвет повторяется 3 раза подряд
# - Новый кортеж с цветами в обратном порядке
#
# **Ограничения**: Кортеж содержит минимум 3 цвета
#
# **Примеры**:
# Input: colors = ("красный", "синий", "зеленый", "желтый")
# Output:
# Общее количество цветов: 4
# Второй цвет: синий
# Предпоследний цвет: зеленый
# Повторяющиеся цвета: ("красный", "красный", "красный", "синий", "синий", "синий", "зеленый", "зеленый", "зеленый", "желтый", "желтый", "желтый")
# Обратный порядок: ("желтый", "зеленый", "синий", "красный")
#
# Входные данные: colors = ("белый", "черный", "серый")
# Output:
# Общее количество цветов: 3
# Второй цвет: черный
# Предпоследний цвет: черный
# Повторяющиеся цвета: ("белый", "белый", "белый", "черный", "черный", "черный", "серый", "серый", "серый")
# Обратный порядок: ("серый", "черный", "белый")

colors = ("красный", "синий", "зеленый", "желтый")

# - Общее количество цветов в кортеже
print(f"Общее количество цветов: {len(colors)}")
# - Второй цвет в кортеже
print(f"Второй цвет: {colors[1]}")
# - Предпоследний цвет в кортеже
print(f"Предпоследний цвет: {colors[-2]}")
# - Новый кортеж где каждый цвет повторяется 3 раза подряд
print(
    f"Повторяющиеся цвета: {tuple(color for color in colors for _ in range(3))}")
# - Новый кортеж с цветами в обратном порядке
print(f"Обратный порядок: {colors[::-1]}")

# ============================================================================
# ПОДРОБНОЕ ОБЪЯСНЕНИЕ КОДА НА СТРОКАХ 40-41:
# ============================================================================
# 
# tuple(color for color in colors for _ in range(3))
# 
# Это конструкция называется "генератор кортежа" (tuple comprehension) 
# с вложенным циклом.
# 
# РАЗБОР ПО ЧАСТЯМ:
# 
# 1. tuple(...) 
#    - Преобразует генератор в кортеж (неизменяемый список)
# 
# 2. color for color in colors
#    - ВНЕШНИЙ цикл: перебирает каждый элемент из кортежа colors
#    - Для colors = ("красный", "синий", "зеленый", "желтый")
#    - color будет последовательно: "красный", "синий", "зеленый", "желтый"
# 
# 3. for _ in range(3)
#    - ВНУТРЕННИЙ цикл: повторяет действие 3 раза
#    - Символ _ означает, что значение счетчика не используется
#    - range(3) создает последовательность: 0, 1, 2 (всего 3 итерации)
# 
# КАК ЭТО РАБОТАЕТ:
# 
# Для каждого цвета из colors выполняется внутренний цикл 3 раза:
# 
# Итерация 1: color = "красный"
#   - range(3) дает 3 итерации → "красный" добавляется 3 раза
#   Результат: "красный", "красный", "красный"
# 
# Итерация 2: color = "синий"
#   - range(3) дает 3 итерации → "синий" добавляется 3 раза
#   Результат: "синий", "синий", "синий"
# 
# Итерация 3: color = "зеленый"
#   - range(3) дает 3 итерации → "зеленый" добавляется 3 раза
#   Результат: "зеленый", "зеленый", "зеленый"
# 
# Итерация 4: color = "желтый"
#   - range(3) дает 3 итерации → "желтый" добавляется 3 раза
#   Результат: "желтый", "желтый", "желтый"
# 
# ИТОГОВЫЙ РЕЗУЛЬТАТ:
# ("красный", "красный", "красный", "синий", "синий", "синий", 
#  "зеленый", "зеленый", "зеленый", "желтый", "желтый", "желтый")
# 
# ============================================================================
# РАЗВЕРНУТЫЙ КОД БЕЗ ГЕНЕРАТОРА (эквивалентный вариант):
# ============================================================================

# Создаем пустой список для хранения результата
result_list = []

# Внешний цикл: перебираем каждый цвет из кортежа colors
for color in colors:
    # Внутренний цикл: повторяем каждый цвет 3 раза
    for _ in range(3):
        # Добавляем цвет в список
        result_list.append(color)

# Преобразуем список в кортеж
result_tuple = tuple(result_list)

print(f"\n--- РАЗВЕРНУТЫЙ ВАРИАНТ ---")
print(f"Повторяющиеся цвета (развернутый код): {result_tuple}")

# ============================================================================
# АЛЬТЕРНАТИВНЫЙ ВАРИАНТ (еще более явный):
# ============================================================================

result_list_alt = []
for i in range(len(colors)):  # Перебираем индексы цветов
    current_color = colors[i]  # Берем цвет по индексу
    for j in range(3):  # Повторяем 3 раза
        result_list_alt.append(current_color)
result_tuple_alt = tuple(result_list_alt)

print(f"Повторяющиеся цвета (альтернативный вариант): {result_tuple_alt}")

# ============================================================================
# ЕЩЕ ОДИН ВАРИАНТ (с использованием умножения списка):
# ============================================================================

result_list_mult = []
for color in colors:
    # Создаем список из 3 одинаковых цветов и добавляем в результат
    result_list_mult.extend([color] * 3)
result_tuple_mult = tuple(result_list_mult)

print(f"Повторяющиеся цвета (с умножением списка): {result_tuple_mult}")
