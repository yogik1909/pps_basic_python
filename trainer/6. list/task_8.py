# **Описание**: Создайте программу для работы с данными о музыкальных инструментах
#
# **Входные данные**: Список instruments содержащий названия музыкальных инструментов
#
# **Выходные данные**:
# - Общее количество инструментов в списке
# - Новый список с инструментами, отсортированными в алфавитном порядке
# - Новый список с инструментами в обратном алфавитном порядке
# - Новый список с инструментами, где каждое название повторяется 2 раза подряд
#
# **Ограничения**: Список содержит минимум 3 инструмента
#
# **Примеры**:
# Input: instruments = ["гитара", "барабан", "флейта", "арфа"]
# Output:
# Общее количество инструментов: 4
# Алфавитный порядок: ["арфа", "барабан", "гитара", "флейта"]
# Обратный алфавитный порядок: ["флейта", "гитара", "барабан", "арфа"]
# Повторяющиеся названия: ["гитара", "гитара", "барабан", "барабан", "флейта", "флейта", "арфа", "арфа"]
#
# Входные данные: instruments = ["пианино", "скрипка", "труба"]
# Output:
# Общее количество инструментов: 3
# Алфавитный порядок: ["пианино", "скрипка", "труба"]
# Обратный алфавитный порядок: ["труба", "скрипка", "пианино"]
# Повторяющиеся названия: ["пианино", "пианино", "скрипка", "скрипка", "труба", "труба"]

instruments = ["гитара", "барабан", "флейта", "арфа"]

# Ваш код здесь
# - Общее количество инструментов в списке
print(f"Общее количество инструментов: {len(instruments)}")
# - Новый список с инструментами, отсортированными в алфавитном порядке
print(f"Алфавитный порядок: {sorted(instruments)}")
# - Новый список с инструментами в обратном алфавитном порядке
print(f"Обратный алфавитный порядок: {sorted(instruments, reverse=True)}")
# - Новый список с инструментами, где каждое название повторяется 2 раза подряд
print(
    f"Повторяющиеся названия: {tuple(instrument for instrument in instruments for _ in range(2))}")
