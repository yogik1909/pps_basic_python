# **Описание**: Создайте программу для работы со списком температур и их преобразованием
#
# **Входные данные**: Список temperatures содержащий значения температур в градусах Цельсия
#
# **Выходные данные**:
# - Новый список с температурами в Фаренгейтах (формула: F = C * 9/5 + 32)
# - Количество положительных температур в исходном списке
# - Первая и последняя температура из исходного списка
#
# **Ограничения**: Список содержит минимум 3 элемента
#
# **Примеры**:
# Input: temperatures = [0, 25, -10, 15]
# Output:
# Температуры в Фаренгейтах: [32.0, 77.0, 14.0, 59.0]
# Количество положительных: 2
# Первая температура: 0
# Последняя температура: 15
#
# Входные данные: temperatures = [10, -5, 20]
# Output:
# Температуры в Фаренгейтах: [50.0, 23.0, 68.0]
# Количество положительных: 2
# Первая температура: 10
# Последняя температура: 20

temperatures = [0, 25, -10, 15]

# Ваш код здесь
fahrenheit_temps = [C * 9/5 + 32 for C in temperatures]
positive_count = len([C for C in temperatures if C > 0])
first_temp = temperatures[0]
last_temp = temperatures[-1]

print(f"Температуры в Фаренгейтах: {fahrenheit_temps}")
print(f"Количество положительных: {positive_count}")
print(f"Первая температура: {first_temp}")
print(f"Последняя температура: {last_temp}")
