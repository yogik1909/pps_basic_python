# **Описание**: Создайте программу для работы с кортежем координат точек на плоскости
#
# **Входные данные**: Кортеж coordinates содержащий координаты точек в формате (x1, y1, x2, y2, x3, y3, ...)
#
# **Выходные данные**:
# - Общее количество координат
# - Первая координата x
# - Последняя координата y
# - Новый кортеж только с координатами x (четные позиции)
#
# **Ограничения**: Кортеж содержит четное количество элементов (минимум 4)
#
# **Примеры**:
# Input: coordinates = (10, 20, 30, 40, 50, 60)
# Output:
# Общее количество координат: 6
# Первая координата x: 10
# Последняя координата y: 60
# Координаты x: (10, 30, 50)
#
# Входные данные: coordinates = (1, 2, 3, 4)
# Output:
# Общее количество координат: 4
# Первая координата x: 1
# Последняя координата y: 4
# Координаты x: (1, 3)

# Данные для работы
coordinates = (10, 20, 30, 40, 50, 60)

print(f"Общее количество координат: {len(coordinates)}")
print(f"Первая координата x: {coordinates[0]}")
print(f"Последняя координата y: {coordinates[-1]}")
print(f"Координаты x: {coordinates[::2]}")
