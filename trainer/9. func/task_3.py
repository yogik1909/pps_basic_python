# **Описание**: Реализуйте функцию, принимающую произвольное число позиционных аргументов через *args и возвращающую их сумму.
#
# **Входные данные**: Функция должна принимать произвольное количество числовых аргументов через *args
#
# **Выходные данные**: Функция должна возвращать сумму всех переданных аргументов. Если аргументы не переданы, функция должна возвращать 0
#
# **Ограничения**: Используйте только базовые арифметические операции и встроенную функцию sum()
#
# **Примеры**:
# Вызов: sum_all(1, 2, 3)
# Возврат: 6
#
# Вызов: sum_all(10, 20)
# Возврат: 30
#
# Вызов: sum_all()
# Возврат: 0

def sum_all(*args: int) -> int:
    return sum(args) if args else 0


print(sum_all(1, 2, 3))
print(sum_all(10, 20))
print(sum_all())
