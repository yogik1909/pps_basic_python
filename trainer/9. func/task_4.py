# **Описание**: Создайте функцию, которая возвращает None в случае отсутствия результата и проверьте это с помощью оператора is None.
#
# **Входные данные**: Функция должна принимать список чисел
#
# **Выходные данные**: Функция должна возвращать первое отрицательное число из списка или None, если отрицательных чисел нет
#
# **Ограничения**: Используйте только базовые операции со списками и оператор is None для проверки
#
# **Примеры**:
# Вызов: find_negative([1, 2, -3, 4])
# Возврат: -3
#
# Вызов: find_negative([1, 2, 3])
# Возврат: None

def find_negative(numbers: list[int]) -> int | None:
    # Ваш код здесь
    for number in numbers:
        if number < 0:
            return number
    return None


# Тестовые данные
test_list1 = [1, 2, -3, 4]
test_list2 = [1, 2, 3]

# Вызовы функции
result1 = find_negative(test_list1)
result2 = find_negative(test_list2)

# Проверка с is None
if result1 is None:
    print("Первый список: отрицательных чисел нет")
else:
    print(f"Первый список: найдено отрицательное число {result1}")

if result2 is None:
    print("Второй список: отрицательных чисел нет")
else:
    print(f"Второй список: найдено отрицательное число {result2}")
