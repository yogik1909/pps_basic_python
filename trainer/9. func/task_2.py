# **Описание**: Создайте функцию с аргументом по умолчанию, которая принимает имя пользователя и необязательный префикс приветствия, и возвращает персонализированное сообщение.
#
# **Входные данные**: Функция должна принимать обязательный аргумент name (строка) и необязательный аргумент greeting (строка, по умолчанию "Hello")
#
# **Выходные данные**: Функция должна возвращать строку в формате "{greeting}, {name}!"
#
# **Ограничения**: Используйте только базовые операции со строками
#
# **Примеры**:
# Вызов: greet_user("Alice")
# Возврат: "Hello, Alice!"
#
# Вызов: greet_user("Bob", "Hi")
# Возврат: "Hi, Bob!"

def greet_user(name: str, greeting: str = "Hello") -> str:
    return f"{greeting}, {name}!"


print(greet_user("Alice"))
print(greet_user("Bob", "Hi"))
