# **Описание**: Создайте функцию, которая принимает именованные аргументы для создания персонализированного сообщения о покупке товара.
#
# **Входные данные**: Функция должна принимать именованные аргументы: item (название товара), price (цена), quantity (количество, по умолчанию 1)
#
# **Выходные данные**: Функция должна возвращать строку в формате "Заказ: {quantity} x {item} по {price} руб. = {total} руб."
#
# **Ограничения**: Используйте только базовые арифметические операции и форматирование строк
#
# **Примеры**:
# Вызов: create_order(item="Книга", price=500)
# Возврат: "Заказ: 1 x Книга по 500 руб. = 500 руб."
#
# Вызов: create_order(item="Ручка", price=50, quantity=3)
# Возврат: "Заказ: 3 x Ручка по 50 руб. = 150 руб."

def create_order(item: str, price: int, quantity: int = 1) -> str:
    # Ваш код здесь
    return f"Заказ: {quantity} x {item} по {price} руб. = {quantity * price} руб."


print(create_order(item="Книга", price=500))
print(create_order(item="Ручка", price=50, quantity=3))
