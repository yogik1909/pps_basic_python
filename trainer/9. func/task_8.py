# **Описание**: Создайте функцию, которая принимает произвольное количество именованных аргументов через **kwargs и возвращает строку с информацией о переданных параметрах.
#
# **Входные данные**: Функция должна принимать произвольное количество именованных аргументов через **kwargs
#
# **Выходные данные**: Функция должна возвращать строку в формате "Получено {количество} параметров: {список_имен}", где список имен разделен запятыми
#
# **Ограничения**: Используйте только базовые операции со строками и встроенные функции len() и join()
#
# **Примеры**:
# Вызов: describe_params(name="Alice", age=25, city="Moscow")
# Возврат: "Получено 3 параметров: name, age, city"
#
# Вызов: describe_params(color="red")
# Возврат: "Получено 1 параметров: color"

def describe_params(**kwargs: str) -> str:
    return f"Получено {len(kwargs)} параметров: {', '.join(kwargs.keys())}"


# Тестовые вызовы
print(describe_params(name="Alice", age=25, city="Moscow"))
print(describe_params(color="red"))
