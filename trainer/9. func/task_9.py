# **Описание**: Создайте функцию, которая принимает произвольное количество позиционных и именованных аргументов и возвращает количество всех переданных параметров.
#
# **Входные данные**: Функция должна принимать произвольное количество позиционных аргументов через *args и именованных аргументов через **kwargs
#
# **Выходные данные**: Функция должна возвращать общее количество всех переданных аргументов (позиционных + именованных)
#
# **Ограничения**: Используйте только базовые операции подсчета и встроенную функцию len()
#
# **Примеры**:
# Вызов: count_all_args(1, 2, 3, name="Alice", age=25)
# Возврат: 5
#
# Вызов: count_all_args("hello", city="Moscow")
# Возврат: 2
#
# Вызов: count_all_args()
# Возврат: 0

def count_all_args(*args: int, **kwargs: str) -> int:
    # Ваш код здесь
    return len(args) + len(kwargs)


# Тестовые вызовы
print(count_all_args(1, 2, 3, name="Alice", age=25))
print(count_all_args("hello", city="Moscow"))
print(count_all_args())
