# **Описание**: Разбейте небольшой фрагмент кода на две функции: одна получает ввод пользователя, другая обрабатывает и возвращает результат.
#
# **Входные данные**: Создайте две функции:
# 1. Функция get_temperature() - запрашивает у пользователя температуру в Цельсиях через input()
# 2. Функция celsius_to_fahrenheit(celsius) - принимает температуру в Цельсиях и возвращает температуру в Фаренгейтах
#
# **Выходные данные**:
# - get_temperature() должна возвращать число (float)
# - celsius_to_fahrenheit() должна возвращать температуру в Фаренгейтах (float)
# - Основной код должен использовать обе функции и выводить результат
#
# **Ограничения**: Используйте только базовые арифметические операции и функцию input()
#
# **Примеры**:
# Ввод пользователя: 0
# Вывод: 32.0
#
# Ввод пользователя: 100
# Вывод: 212.0

def get_temperature():
    return float(input("Введите температуру в Цельсиях: "))


def celsius_to_fahrenheit(celsius: float) -> float:
    return celsius * 9/5 + 32


print(celsius_to_fahrenheit(get_temperature()))
