# **Описание**: Создайте программу для работы с множеством активных пользователей и их ролей с использованием операций добавления и удаления элементов
#
# **Входные данные**: Множество активных пользователей (встроенное в код)
#
# **Выходные данные**: Множество после добавления нового пользователя, множество после удаления пользователя с помощью discard, и размер итогового множества
#
# **Ограничения**: Используйте только встроенные методы множеств Python (add, discard, len)
#
# **Примеры**:
# Входные данные: {'admin', 'moderator', 'user'}
# Добавить: 'guest'
# Удалить: 'user'
# Выходные данные:
# После добавления: {'admin', 'moderator', 'user', 'guest'}
# После удаления: {'admin', 'moderator', 'guest'}
# Размер множества: 3
#
# Входные данные: {'editor', 'viewer'}
# Добавить: 'contributor'
# Удалить: 'admin' (элемента нет)
# Выходные данные:
# После добавления: {'editor', 'viewer', 'contributor'}
# После удаления: {'editor', 'viewer', 'contributor'}
# Размер множества: 3

# Инициализация множества активных пользователей
активные_пользователи = {'admin', 'moderator', 'user'}

# Ваш код здесь
активные_пользователи.add('guest')
активные_пользователи.discard('user')
print(f"После добавления: {активные_пользователи}")
print(f"После удаления: {активные_пользователи}")
print(f"Размер множества: {len(активные_пользователи)}")
