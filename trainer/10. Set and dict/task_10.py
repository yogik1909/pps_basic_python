# **Описание**: Создайте программу для работы со словарем продуктов и их цен с использованием итерации по ключам и значениям
#
# **Входные данные**: Словарь продуктов с названиями и ценами (встроенный в код)
#
# **Выходные данные**: Список всех названий продуктов, список всех цен, и общая сумма всех цен
#
# **Ограничения**: Используйте только базовые операции со словарями Python и методы keys(), values()
#
# **Примеры**:
# Входные данные: {'молоко': 65, 'хлеб': 45, 'яйца': 85}
# Выходные данные:
# Продукты: ['молоко', 'хлеб', 'яйца']
# Цены: [65, 45, 85]
# Общая стоимость: 195
#
# Входные данные: {'сыр': 320, 'масло': 180}
# Выходные данные:
# Продукты: ['сыр', 'масло']
# Цены: [320, 180]
# Общая стоимость: 500

# Ваш код здесь
products = {'молоко': 65, 'хлеб': 45, 'яйца': 85}
print(f"Продукты: {list(products.keys())}")
print(f"Цены: {list(products.values())}")
print(f"Общая стоимость: {sum(products.values())}")
