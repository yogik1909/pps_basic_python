# **Описание**: Создайте программу для работы со словарем книг и их авторов с использованием метода pop
#
# **Входные данные**: Словарь книг с названиями и авторами (встроенный в код)
#
# **Выходные данные**: Удаленная книга с автором и обновленный словарь после удаления
#
# **Ограничения**: Используйте только базовые операции со словарями Python и метод pop
#
# **Примеры**:
# Входные данные: {'1984': 'Джордж Оруэлл', 'Мастер и Маргарита': 'Михаил Булгаков', 'Война и мир': 'Лев Толстой'}
# Удалить: '1984'
# Выходные данные:
# Удаленная книга: 1984 - Джордж Оруэлл
# Оставшиеся книги: {'Мастер и Маргарита': 'Михаил Булгаков', 'Война и мир': 'Лев Толстой'}
#
# Входные данные: {'Гарри Поттер': 'Дж. К. Роулинг', 'Хоббит': 'Дж. Р. Р. Толкин'}
# Удалить: 'Хоббит'
# Выходные данные:
# Удаленная книга: Хоббит - Дж. Р. Р. Толкин
# Оставшиеся книги: {'Гарри Поттер': 'Дж. К. Роулинг'}

# Словарь книг
books = {'1984': 'Джордж Оруэлл',
         'Мастер и Маргарита': 'Михаил Булгаков', 'Война и мир': 'Лев Толстой'}

del_key = '1984'
del_value = books.pop(del_key)


# Ваш код здесь
print(f"Удаленная книга: {del_key} - {del_value}")
print(f"Оставшиеся книги: {books}")
