# **Описание**: Создайте программу для работы с множеством категорий товаров и их копированием
#
# **Входные данные**: Множество категорий товаров (встроенное в код)
#
# **Выходные данные**: Исходное множество, скопированное множество и демонстрация их независимости
#
# **Ограничения**: Используйте только встроенные методы множеств Python (copy, add)
#
# **Примеры**:
# Входные данные: {'электроника', 'одежда', 'книги'}
# Выходные данные:
# Исходное множество: {'электроника', 'одежда', 'книги'}
# Скопированное множество: {'электроника', 'одежда', 'книги'}
# После добавления 'игрушки' в копию:
# Исходное множество: {'электроника', 'одежда', 'книги'}
# Скопированное множество: {'электроника', 'одежда', 'книги', 'игрушки'}
#
# Входные данные: {'спорт', 'дом'}
# Выходные данные:
# Исходное множество: {'спорт', 'дом'}
# Скопированное множество: {'спорт', 'дом'}
# После добавления 'автомобили' в копию:
# Исходное множество: {'спорт', 'дом'}
# Скопированное множество: {'спорт', 'дом', 'автомобили'}

# Ваш код здесь
categories = {'электроника', 'одежда', 'книги'}
copied_categories = categories.copy()
copied_categories.add('игрушки')
print(f"Исходное множество: {categories}")
print(f"Скопированное множество: {copied_categories}")
