# **Описание**: Создайте программу для проверки подмножества и надмножества между двумя множествами студентов
#
# **Входные данные**: Два множества с именами студентов (встроенные в код)
#
# **Выходные данные**: Результаты проверки issubset и issuperset между множествами
#
# **Ограничения**: Используйте только встроенные методы множеств Python
#
# **Примеры**:
# Входные данные:
# студенты_группа1 = {'Анна', 'Борис', 'Виктор'}
# студенты_группа2 = {'Анна', 'Борис', 'Виктор', 'Галина', 'Дмитрий'}
#
# Выходные данные:
# Группа1 является подмножеством Группы2: True
# Группа2 является надмножеством Группы1: True
# Группа2 является подмножеством Группы1: False

# Встроенные данные
студенты_группа1 = {'Анна', 'Борис', 'Виктор'}
студенты_группа2 = {'Анна', 'Борис', 'Виктор', 'Галина', 'Дмитрий'}

# Ваш код здесь
print(
    f"Группа1 является подмножеством Группы2: {студенты_группа1.issubset(студенты_группа2)}")
print(
    f"Группа2 является надмножеством Группы1: {студенты_группа2.issuperset(студенты_группа1)}")
print(
    f"Группа2 является подмножеством Группы1: {студенты_группа2.issubset(студенты_группа1)}")
