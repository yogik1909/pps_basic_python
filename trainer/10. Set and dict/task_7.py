# **Описание**: Создайте программу для работы со словарем настроек приложения с использованием безопасного получения значений
#
# **Входные данные**: Словарь настроек приложения (встроенный в код)
#
# **Выходные данные**: Значения настроек с использованием метода get и значений по умолчанию
#
# **Ограничения**: Используйте только базовые операции со словарями Python и метод get
#
# **Примеры**:
# Входные данные: {'theme': 'dark', 'language': 'ru', 'notifications': True}
# Получить: 'theme', 'font_size' (по умолчанию 14), 'auto_save' (по умолчанию False)
# Выходные данные:
# Тема: dark
# Размер шрифта: 14
# Автосохранение: False
#
# Входные данные: {'volume': 80, 'quality': 'high'}
# Получить: 'volume', 'brightness' (по умолчанию 50), 'contrast' (по умолчанию 100)
# Выходные данные:
# Громкость: 80
# Яркость: 50
# Контрастность: 100

# Словарь настроек приложения
настройки = {'theme': 'dark', 'language': 'ru', 'notifications': True}


# Ваш код здесь
print(f"Тема: {настройки.get('theme')}")
print(f"Размер шрифта: {настройки.get('font_size', 14)}")
print(f"Автосохранение: {настройки.get('auto_save', False)}")
