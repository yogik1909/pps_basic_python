# **Описание**: Создайте программу для работы с множеством тегов статей блога
#
# **Входные данные**: Два множества тегов (встроенные в код)
#
# **Выходные данные**: Результаты операций пересечения, объединения и разности между множествами
#
# **Ограничения**: Используйте только встроенные методы множеств Python
#
# **Примеры**:
# Входные данные:
# теги_статьи1 = {'python', 'программирование', 'веб'}
# теги_статьи2 = {'python', 'данные', 'анализ'}
#
# Выходные данные:
# Пересечение: {'python'}
# Объединение: {'python', 'программирование', 'веб', 'данные', 'анализ'}
# Разность (статья1 - статья2): {'программирование', 'веб'}

# Встроенные данные
теги_статьи1 = {'python', 'программирование', 'веб'}
теги_статьи2 = {'python', 'данные', 'анализ'}

# Ваш код здесь
print(f"Пересечение: {теги_статьи1 & теги_статьи2}")
print(f"Объединение: {теги_статьи1 | теги_статьи2}")
print(f"Разность (статья1 - статья2): {теги_статьи1 - теги_статьи2}")
