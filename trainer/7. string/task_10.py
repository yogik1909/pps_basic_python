# **Описание**: Создайте программу для проверки длины строки и её содержимого с помощью функции len() и методов строк
# **Входные данные**: Используйте встроенную переменную text с текстовым значением
# **Выходные данные**: Длина строки и результаты проверок содержимого
# **Ограничения**: Используйте только функцию len() и методы строк isalpha(), isspace()

# Встроенная переменная с текстом
text = "Привет мир"

print(f"Длина строки: {len(text)}")
print(f"Содержит только буквы: {text.isalpha()}")
print(f"Содержит только пробелы: {text.isspace()}")
