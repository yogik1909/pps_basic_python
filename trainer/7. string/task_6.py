# **Описание**: Создайте программу для получения кодов символов с помощью функции ord()
# **Входные данные**: Используйте встроенные переменные с символами для анализа
# **Выходные данные**: Коды символов в формате "Символ 'X' имеет код: Y"
# **Ограничения**: Используйте только функцию ord() для получения кодов символов
# **Примеры**:
# Входные данные: char1 = 'A', char2 = 'z', char3 = '5'
# Выходные данные:
# Символ 'A' имеет код: 65
# Символ 'z' имеет код: 122
# Символ '5' имеет код: 53
# Входные данные: char1 = '@', char2 = ' ', char3 = 'Я'
# Выходные данные:
# Символ '@' имеет код: 64
# Символ ' ' имеет код: 32
# Символ 'Я' имеет код: 1071

# Встроенные переменные с символами
char1 = 'A'
char2 = 'z'
char3 = '5'

print(f"Символ '{char1}' имеет код: {ord(char1)}")
print(f"Символ '{char2}' имеет код: {ord(char2)}")
print(f"Символ '{char3}' имеет код: {ord(char3)}")