# **Описание**: Создайте программу для работы с переменными разных типов данных
# **Входные данные**: Имя пользователя (строка), возраст (целое число), рост (вещественное число)
# **Выходные данные**: Информация о пользователе с указанием типов данных
# **Ограничения**: Возраст от 1 до 120 лет, рост от 0.5 до 3.0 метров
#
# Input: "Анна", 25, 1.65
# Output: Имя: Анна (тип: <class 'str'>), Возраст: 25 (тип: <class 'int'>), Рост: 1.65 (тип: <class 'float'>)
#
# Input: "Петр", 30, 1.80
# Output: Имя: Петр (тип: <class 'str'>), Возраст: 30 (тип: <class 'int'>), Рост: 1.80 (тип: <class 'float'>)

def main():
    name = input("Введите имя: ")
