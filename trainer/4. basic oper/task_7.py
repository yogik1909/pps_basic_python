# **Описание**: Создайте программу для работы с битовыми операторами и демонстрации их влияния на числа
#
# **Входные данные**: Используйте встроенные переменные в коде:
# - Первое число (целое)
# - Второе число (целое)
#
# **Выходные данные**: Выведите на экран результаты всех битовых операций:
# - Результат побитового И (&)
# - Результат побитового ИЛИ (|)
# - Результат побитового исключающего ИЛИ (^)
# - Результат побитового НЕ (~) для первого числа
# - Результат побитового НЕ (~) для второго числа
# - Результат сдвига первого числа влево на 2 позиции (<<)
# - Результат сдвига второго числа вправо на 1 позицию (>>)
#
# **Ограничения**:
# - Используйте только битовые операторы (&, |, ^, ~, <<, >>)
# - Результат каждой операции сохраните в отдельную переменную
# - Числа от 1 до 50
# - Все переменные должны иметь понятные имена

dig_first = 1
dig_second = 50

logical_and = dig_first & dig_second

print(f"Результат побитового И: {logical_and}")

logical_or = dig_first | dig_second
print(f"Результат побитового ИЛИ: {logical_or}")

logical_xor = dig_first ^ dig_second
print(f"Результат побитового mисключающего ИЛИ: {logical_xor}")

logical_not_first = ~dig_first
logical_not_second = ~dig_second
print(f"Результат побитового НЕ для первого числа: {logical_not_first}")
print(f"Результат побитового НЕ для второго числа: {logical_not_second}")

logical_left_shift = dig_first << 2
print(f"Результат сдвига первого числа влево на 2 позиции: {logical_left_shift}")

logical_right_shift = dig_second >> 1
print(f"Результат сдвига второго числа вправо на 1 позицию: {logical_right_shift}")