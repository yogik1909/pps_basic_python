# **Описание**: Создайте программу для работы с порядком операторов и демонстрации влияния скобок на результат вычислений
#
# **Входные данные**: Используйте встроенные переменные в коде:
# - Первое число (целое)
# - Второе число (целое)
# - Третье число (целое)
#
# **Выходные данные**: Выведите на экран результаты вычислений:
# - Результат выражения без скобок: a + b * c
# - Результат выражения со скобками: (a + b) * c
# - Результат выражения без скобок: a * b + c ** 2
# - Результат выражения со скобками: a * (b + c) ** 2
# - Результат выражения без скобок: a / b + c
# - Результат выражения со скобками: a / (b + c)
#
# **Ограничения**:
# - Используйте только арифметические операторы (+, -, *, /, **)
# - Результат каждого выражения сохраните в отдельную переменную
# - Числа от 2 до 20
# - Избегайте деления на ноль
#
# **Примеры**:
# Входные данные: a = 4, b = 3, c = 2
# Выходные данные:
# a + b * c = 10
# (a + b) * c = 14
# a * b + c ** 2 = 16
# a * (b + c) ** 2 = 100
# a / b + c = 3.3333333333333335
# a / (b + c) = 0.8
#
# Входные данные: a = 6, b = 2, c = 4
# Выходные данные:
# a + b * c = 14
# (a + b) * c = 32
# a * b + c ** 2 = 28
# a * (b + c) ** 2 = 216
# a / b + c = 7.0
# a / (b + c) = 1.0

a, b, c = 4, 3, 2

result_1 = a + b * c
result_2 = (a + b) * c
result_3 = a * b + c ** 2
result_4 = a * (b + c) ** 2
result_5 = a / b + c
result_6 = a / (b + c)

print(f"Результат выражения без скобок: a + b * c: {result_1}")
print(f"Результат выражения со скобками: (a + b) * c: {result_2}")
print(f"Результат выражения без скобок: a * b + c ** 2: {result_3}")
print(f"Результат выражения со скобками: a * (b + c) ** 2: {result_4:.2f}")
print(f"Результат выражения без скобок: a / b + c: {result_5}")
print(f"Результат выражения со скобками: a / (b + c): {result_6}")
