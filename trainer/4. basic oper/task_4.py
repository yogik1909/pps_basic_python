# **Описание**: Создайте программу для работы с сокращёнными операторами присваивания и демонстрации их эффектов
#
# **Входные данные**: Используйте встроенные переменные в коде:
# - Начальное значение первой переменной (целое число)
# - Начальное значение второй переменной (целое число)
# - Начальное значение третьей переменной (целое число)
#
# **Выходные данные**: Выведите на экран:
# - Значения всех переменных до изменений
# - Значения после применения += к первой переменной
# - Значения после применения -= ко второй переменной
# - Значения после применения *= к третьей переменной
# - Значения после применения /= к первой переменной
# - Значения после применения %= ко второй переменной
#
# **Ограничения**:
# - Используйте только сокращённые операторы присваивания (+=, -=, *=, /=, %=)
# - Каждое изменение должно быть в отдельной строке кода
# - Начальные значения от 10 до 50
# - Операнды для изменений от 2 до 8

a, b, c = 10, 20, 30

print(f"Значения всех переменных до изменений: a = {a}, b = {b}, c = {c}")
a += 2
print(f"Значения после a += 2: a = {a}, b = {b}, c = {c}")
b -= 3
print(f"Значения после b -= 3: a = {a}, b = {b}, c = {c}")
c *= 4
print(f"Значения после c *= 4: a = {a}, b = {b}, c = {c}")
a /= 5
print(f"Значения после a /= 5: a = {a}, b = {b}, c = {c}")
b %= 6
print(f"Значения после b %= 6: a = {a}, b = {b}, c = {c}")
