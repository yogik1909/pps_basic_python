# **Описание**: Создайте программу для вычисления площади и периметра прямоугольника с заданными размерами
#
# **Входные данные**: Используйте встроенные переменные в коде:
# - Длина прямоугольника (число)
# - Ширина прямоугольника (число)
#
# **Выходные данные**: Выведите на экран:
# - Длину прямоугольника
# - Ширину прямоугольника
# - Площадь прямоугольника
# - Периметр прямоугольника
# - Отношение длины к ширине
#
# **Ограничения**: 
# - Используйте только арифметические операции
# - Каждое вычисление должно быть выполнено в отдельной переменной
# - Длина и ширина от 1 до 20
# - Все результаты должны быть числовыми

# Входные данные
length = 10
width = 5

# Вычисления
area = length * width
perimeter = 2 * (length + width)
ratio = length / width

print(f"Длина прямоугольника: {length}")
print(f"Ширина прямоугольника: {width}")
print(f"Площадь прямоугольника: {area}")
print(f"Периметр прямоугольника: {perimeter}")
print(f"Отношение длины к ширине: {ratio:.2f}")