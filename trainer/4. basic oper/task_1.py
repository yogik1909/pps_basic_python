# **Описание**: Создайте программу для вычисления стоимости покупки с применением скидки
#
# **Входные данные**: Используйте встроенные переменные в коде:
# - Цена товара (число)
# - Размер скидки в процентах (число)
#
# **Выходные данные**: Выведите на экран:
# - Первоначальную цену
# - Размер скидки в процентах
# - Сумму скидки в рублях
# - Итоговую цену после применения скидки
#
# **Ограничения**:
# - Используйте только арифметические операции
# - Все вычисления должны быть выполнены в отдельных переменных
# - Цена товара от 100 до 10000 рублей
# - Скидка от 5 до 50 процентов
#
# **Примеры**:
# Входные данные: цена = 1000, скидка = 15
# Выходные данные:
# Первоначальная цена: 1000
# Скидка: 15%
# Сумма скидки: 150.0
# Итоговая цена: 850.0
#
# Входные данные: цена = 500, скидка = 20
# Выходные данные:
# Первоначальная цена: 500
# Скидка: 20%
# Сумма скидки: 100.0
# Итоговая цена: 400.0

# Встроенные данные
price = 1000
discount_percent = 15
discount_sum = price * discount_percent / 100
price_after_discount = price - discount_sum

print(f"Первоначальная цена: {price}")
print(f"Скидка: {discount_percent}%")
print(f"Сумма скидки: {discount_sum:.1f}")
print(f"Итоговая цена: {price_after_discount:.1f}")
