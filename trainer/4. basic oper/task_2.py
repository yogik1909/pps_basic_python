# **Описание**: Создайте программу для сравнения двух чисел и определения их соотношения
#
# **Входные данные**: Используйте встроенные переменные в коде:
# - Первое число (целое)
# - Второе число (целое)
#
# **Выходные данные**: Выведите на экран результаты всех операторов сравнения:
# - Равны ли числа (==)
# - Не равны ли числа (!=)
# - Первое больше второго (>)
# - Первое меньше второго (<)
# - Первое больше или равно второму (>=)
# - Первое меньше или равно второму (<=)
#
# **Ограничения**:
# - Используйте только операторы сравнения
# - Результат каждого сравнения сохраните в отдельную булеву переменную
# - Числа от -100 до 100

fig_first = int(input("Введите первое число: "))
fig_second = int(input("Введите второе число: "))

is_equal = fig_first == fig_second
is_not_equal = fig_first != fig_second
is_greater = fig_first > fig_second
is_less = fig_first < fig_second
is_greater_or_equal = fig_first >= fig_second
is_less_or_equal = fig_first <= fig_second

print(f"Равны ли числа: {is_equal}")
print(f"Не равны ли числа: {is_not_equal}")
print(f"Первое больше второго: {is_greater}")
print(f"Первое меньше второго: {is_less}")
print(f"Первое больше или равно второму: {is_greater_or_equal}")
print(f"Первое меньше или равно второму: {is_less_or_equal}")
