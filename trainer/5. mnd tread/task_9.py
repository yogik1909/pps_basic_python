# • Переписать внутренние условия на непротиворечивые, неперекрывающиеся интервалы, например:
#   • 140 <= height <= 159 → "Размер S"
#   • 160 <= height <= 174 → "Размер M"
#   • 175 <= height <= 200 → "Размер L"
# • Заменить последний elif на проверку верхнего интервала (elif height <= 200: print("Размер L")) чтобы покрыть все допустимые значения.
# • Убедиться, что для значений вне диапазона выводится точно "Размер не определен" как в примере (сейчас вывод другой строки), чтобы соответствовать требованиям и примерам.

height = int(input("Введите рост человека в сантиметрах: "))

if height >= 140 and height <= 200:
    if height >= 140 and height <= 159:
        print("Размер S")
    elif height >= 160 and height <= 174:
        print("Размер M")
    elif height >= 175 and height <= 200:
        print("Размер L")
else:
    print("Рост не входит в диапазон от 140 до 200 сантиметров")