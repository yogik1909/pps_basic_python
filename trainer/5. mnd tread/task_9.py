# **Описание**: Создайте программу для определения размера одежды по введенному росту
# **Входные данные**: Целое число - рост человека в сантиметрах (через input)
# **Выходные данные**: Текстовое сообщение с размером одежды
# **Ограничения**: Рост от 140 до 200 сантиметров
# S: 140–159, M: 160–174, L: 175–200
# **Примеры**:
# Вход: 155
# Выход: Размер S
# Вход: 170
# Выход: Размер M
# Вход: 185
# Выход: Размер L
# Вход: 135
# Выход: Размер не определен

