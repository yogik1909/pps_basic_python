# **Описание**: Создайте программу для определения типа треугольника по длинам его сторон
# **Входные данные**: Три целых числа - длины сторон треугольника (через input)
# **Выходные данные**: Текстовое сообщение с типом треугольника
# **Ограничения**: Длины сторон от 1 до 100
# **Примеры**:
# Вход: 3, 3, 3
# Выход: Равносторонний треугольник
# Вход: 5, 5, 8
# Выход: Равнобедренный треугольник
# Вход: 3, 4, 5
# Выход: Разносторонний треугольник
# Вход: 1, 2, 5
# Выход: Не треугольник

edge_1 = int(input("Введите длину первой стороны: "))
edge_2 = int(input("Введите длину второй стороны: "))
edge_3 = int(input("Введите длину третьей стороны: "))

# Проверка неравенства треугольника: сумма любых двух сторон больше третьей
if edge_1 + edge_2 <= edge_3 or edge_1 + edge_3 <= edge_2 or edge_2 + edge_3 <= edge_1:
    print("Не треугольник")
elif edge_1 == edge_2 == edge_3:
    print("Равносторонний треугольник")
elif edge_1 == edge_2 or edge_1 == edge_3 or edge_2 == edge_3:
    print("Равнобедренный треугольник")
else:
    print("Разносторонний треугольник") 