# **Описание**: Создайте программу для определения статуса заказа на основе введенного кода
# **Входные данные**: Строка - код статуса заказа (через input)
# **Выходные данные**: Текстовое сообщение с описанием статуса
# **Ограничения**: Код может быть 'new', 'processing', 'shipped', 'delivered' или любой другой
# **Примеры**:
# Вход: new
# Выход: Заказ создан
# Вход: shipped
# Выход: Заказ отправлен
# Вход: unknown
# Выход: Неизвестный статус

input_code = input("Введите код статуса заказа: ")

if input_code == "new":
    print("Заказ создан")
elif input_code == "processing":
    print("Заказ обрабатывается")
elif input_code == "shipped":
    print("Заказ отправлен")
else:
    print("Неизвестный статус")
