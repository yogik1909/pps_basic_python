# **Описание**: Создайте программу для определения типа транспорта по количеству колес
# **Входные данные**: Целое число - количество колес (через input)
# **Выходные данные**: Текстовое сообщение с типом транспорта
# **Ограничения**: Количество колес от 1 до 10
# **Примеры**:
# Вход: 2
# Выход: Мотоцикл
# Вход: 4
# Выход: Автомобиль
# Вход: 6
# Выход: Грузовик
# Вход: 1
# Выход: Моноколесо
# Вход: 15
# Выход: Неизвестный транспорт

wheel_count = int(input("Введите количество колес: "))

if wheel_count == 1:
    print("Моноколесо")
elif wheel_count == 2:
    print("Мотоцикл")
elif wheel_count == 4:
    print("Автомобиль")
elif wheel_count == 6:
    print("Грузовик")
else:
    print("Неизвестный транспорт")